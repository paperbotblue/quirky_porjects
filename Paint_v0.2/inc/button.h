class Button {
public:
    std::vector<SDL_Rect> buttons;
    Button()
    {
        onButton = false;
        buttonPressed = false;
    }
    void addButton(SDL_Rect b)
    {
        buttons.push_back(b);
    }

    void renderButtons(SDL_Renderer* renderer,SDL_Point* mousePos,bool pendown0)
    {
        for(int i = 0 ; i < buttons.size() ; i++)
        {
            SDL_SetRenderDrawColor(renderer,buttonColours[i].r,buttonColours[i].g,buttonColours[i].b,0xFF);
            SDL_RenderFillRect(renderer,&buttons[i]);
            if(buttons[i].x < mousePos->x && buttons[i].x+buttons[i].w > mousePos->x && buttons[i].y < mousePos->y && buttons[i].y+buttons[i].h > mousePos->y)
            {
                SDL_SetRenderDrawColor(renderer,0x00,0x00,0x00,0xFF);
                SDL_RenderDrawRect(renderer,&buttons[i]);
            }
        }

    }


private:
    bool onButton;
    bool buttonPressed;
};

